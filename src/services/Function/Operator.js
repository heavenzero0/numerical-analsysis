
export const plus = (firstValue, secondValue) => {
   return firstValue + secondValue;
};

export const minus = (firstValue, secondValue) => {
    return firstValue - secondValue;
};

export const product = (firstValue, secondValue) => {
    return firstValue * secondValue;
};

export const divide = (firstValue, secondValue) => {
    return firstValue / secondValue;
};

export const exponent = (firstValue, secondValue) => {
    return Math.pow(firstValue, secondValue);
};

